package club_board;

public class ClubBoard {
	
	private int club_boardID;
	private String club_boardTitle;
	private String userID;
	private String club_boardDate;
	private String club_boardContent;
	private int club_boardAvailable;
	private String club_boardContentText;
	private int topicID;
	private int clubID;
	
	public int getClub_boardID() {
		return club_boardID;
	}
	public void setClub_boardID(int club_boardID) {
		this.club_boardID = club_boardID;
	}
	public String getClub_boardTitle() {
		return club_boardTitle;
	}
	public void setClub_boardTitle(String club_boardTitle) {
		this.club_boardTitle = club_boardTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getClub_boardDate() {
		return club_boardDate;
	}
	public void setClub_boardDate(String club_boardDate) {
		this.club_boardDate = club_boardDate;
	}
	public String getClub_boardContent() {
		return club_boardContent;
	}
	public void setClub_boardContent(String club_boardContent) {
		this.club_boardContent = club_boardContent;
	}
	public int getClub_boardAvailable() {
		return club_boardAvailable;
	}
	public void setClub_boardAvailable(int club_boardAvailable) {
		this.club_boardAvailable = club_boardAvailable;
	}
	public String getClub_boardContentText() {
		return club_boardContentText;
	}
	public void setClub_boardContentText(String club_boardContentText) {
		this.club_boardContentText = club_boardContentText;
	}
	public int getTopicID() {
		return topicID;
	}
	public void setTopicID(int topicID) {
		this.topicID = topicID;
	}
	public int getClubID() {
		return clubID;
	}
	public void setClubID(int clubID) {
		this.clubID = clubID;
	}
}