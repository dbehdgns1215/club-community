package community;

public class Community {
	
	private int communityID;
	private String communityTitle;
	private String userID;
	private String communityDate;
	private String communityContent;
	private int communityAvailable;
	private String communityContentText;
	private int topicID;
	
	public int getCommunityID() {
		return communityID;
	}
	public void setCommunityID(int communityID) {
		this.communityID = communityID;
	}
	public String getCommunityTitle() {
		return communityTitle;
	}
	public void setCommunityTitle(String communityTitle) {
		this.communityTitle = communityTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getCommunityDate() {
		return communityDate;
	}
	public void setCommunityDate(String communityDate) {
		this.communityDate = communityDate;
	}
	public String getCommunityContent() {
		return communityContent;
	}
	public void setCommunityContent(String communityContent) {
		this.communityContent = communityContent;
	}
	public int getCommunityAvailable() {
		return communityAvailable;
	}
	public void setCommunityAvailable(int communityAvailable) {
		this.communityAvailable = communityAvailable;
	}
	public String getCommunityContentText() {
		return communityContentText;
	}
	public void setCommunityContentText(String communityContentText) {
		this.communityContentText = communityContentText;
	}
	public int getTopicID() {
		return topicID;
	}
	public void setTopicID(int topicID) {
		this.topicID = topicID;
	}
	
	
}
